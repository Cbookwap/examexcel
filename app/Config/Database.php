    public function __construct()
    {
        parent::__construct();

        // Override with environment variables if available
        $this->loadEnvironmentConfig();

        // Set dynamic database name if not configured
        if (empty($this->default['database'])) {
            $this->default['database'] = $this->detectDatabaseName();
        }

        // Ensure that we always set the database group to 'tests' if
        // we are currently running an automated test suite, so that
        // we don't overwrite live data on accident.
        if (ENVIRONMENT === 'testing') {
            $this->defaultGroup = 'tests';
        }
    }

    /**
     * Load configuration from environment variables
     */
    private function loadEnvironmentConfig(): void
    {
        // Only load if .env file exists and has database configuration
        if (file_exists(ROOTPATH . '.env')) {
            $envContent = file_get_contents(ROOTPATH . '.env');

            // Check if database configuration is uncommented and set
            if (preg_match('/^database\.default\.hostname\s*=\s*(.+)$/m', $envContent) &&
                preg_match('/^database\.default\.database\s*=\s*(.+)$/m', $envContent)) {

                // Load environment configuration
                $this->default['hostname'] = env('database.default.hostname', $this->default['hostname']);
                $this->default['username'] = env('database.default.username', $this->default['username']);
                $this->default['password'] = env('database.default.password', $this->default['password']);
                $this->default['database'] = env('database.default.database', '');
                $this->default['DBDriver'] = env('database.default.DBDriver', $this->default['DBDriver']);
                $this->default['port'] = (int) env('database.default.port', $this->default['port']);
            }
        }
    }

    /**
     * Auto-detect database name based on application folder
     */
    private function detectDatabaseName(): string
    {
        // Try to get from environment variable first
        $dbName = env('database.default.database');
        if (!empty($dbName)) {
            return $dbName;
        }

        // Get the application folder name as database name
        $appPath = ROOTPATH;
        $folderName = basename($appPath);

        // Clean the folder name to be database-safe
        $dbName = preg_replace('/[^a-zA-Z0-9_]/', '_', $folderName);
        $dbName = strtolower($dbName);

        // Check for common database names based on folder
        $possibleNames = [
            'examexcel',
            $dbName . 'excel',
            $dbName . '_cbt',
            $dbName
        ];

        // Try to connect to each possible database name
        foreach ($possibleNames as $testName) {
            try {
                $testConnection = new \mysqli('localhost', 'root', '', $testName);
                if (!$testConnection->connect_error) {
                    $testConnection->close();
                    return $testName;
                }
            } catch (\Exception $e) {
                // Continue to next name
            }
        }

        // Default fallback
        return 'examexcel';
    }
